    public void shake() {
    final int originalX = getLocation().x;
    final int originalY = getLocation().y;

    new Thread(() -> {
        try {
            for (int i = 0; i < 10; i++) {
                setLocation(originalX + 5, originalY);
                Thread.sleep(5);
                setLocation(originalX - 5, originalY);
                Thread.sleep(5);
            }
            setLocation(originalX, originalY); // balikin ke posisi awal
        } catch (Exception e) {
            e.printStackTrace();
        }
    }).start();
}

